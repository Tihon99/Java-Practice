public class Task6 {
    public static void main(String[] args) {
        double d = 100.9;
        int i = (int)d;
        System.out.println("double to int: " + i);
        
        int large = 130;
        byte b = (byte)large;
        System.out.println("int to byte: " + b);
        
        float f = 3.14f;
        short s = (short)f;
        System.out.println("float to short: " + s);
    }
}
