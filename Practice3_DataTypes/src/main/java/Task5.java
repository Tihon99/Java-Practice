public class Task5 {
    public static void main(String[] args) {
        int i = 5;
        double d = 2.5;
        System.out.println("int + double = " + (i + d));
        
        byte b = 10;
        short s = 20;
        System.out.println("byte + short = " + (b + s));
        
        long l = 1000L;
        float f = 3.5f;
        System.out.println("long + float = " + (l + f));
    }
}
